# 사칙연산

print(2 + 3)  # 5
print(3 * 4)  # 12
# 나누기
print(13 // 2)  # 6
print(13 / 2)  # 6.5
print(11 % 3)  # 2
# 거듭제곱 2의 3승
print(2**3)  # 8
# 우선순위 변경
print((2 + 3) * 2)  # 10

# 소수 연산
print(2.3 * 3.4)  # 7.81999999 출력
print(3 + 1.0)  # 더 정밀한 소수 자료형 4.0으로 변환

# 자료형
print(type(3))  # int
print(type(3.5))  # float
print(type(2 + 3j))  # complex 복소수
print(type(True))  # bool
print(type("abc"))  # str
